class Player():
    def __init__(self, x, y):
        # player animation frames
        self.frames_right = [] 
        self.index = 0
        self.counter = 0
        for i in range(1, 5):
            img_right = pygame.image.load(f'files/images/guy{i}.png')
            img_right = pygame.transform.scale(img_right, (40, 80))
            self.frames_right.append(img_right)

        # player variables
        self.image = self.frames_right[self.index]
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.vel_y = 0
        self.jumped = False

    def update(self):
        dx = 0
        dy = 0
        walk_cooldown = 10


        #get keypresses
        key = pygame.key.get_pressed()
        if key[pygame.K_SPACE] and self.jumped == False:
            self.vel_y = -15
            self.jumped = True
        if key[pygame.K_SPACE] == False:
            self.jumped = False
        if key[pygame.K_LEFT]:
            pygame.transform.rotate(self.image, 50)
            dx -= 5
        if key[pygame.K_RIGHT]:
            dx += 5



        #add gravity
        self.vel_y += 1
        if self.vel_y > 10:
            self.vel_y = 10
        dy += self.vel_y

        #check for collision

        #update player coordinates
        self.rect.x += dx
        self.rect.y += dy

        if self.rect.bottom > screen_height:
            self.rect.bottom = screen_height
            dy = 0

        # handle animation
        self.counter += 1
        if self.counter > walk_cooldown:
            self.index += 1
            self.image = self.frames_right[self.index]
            if self.index >= len(self.frames_right) - 1:
                self.index = 0
            self.counter = 0


        #draw player onto screen
        screen.blit(self.image, self.rect)




class World():
    def __init__(self, data):
        self.tile_list = []

        #load images
        dirt_img = pygame.image.load('files/images/dirt.png')
        grass_img = pygame.image.load('files/images/grass.png')

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(dirt_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                if tile == 2:
                    img = pygame.transform.scale(grass_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                col_count += 1
            row_count += 1

    def draw(self):
        for tile in self.tile_list:
            screen.blit(tile[0], tile[1])



